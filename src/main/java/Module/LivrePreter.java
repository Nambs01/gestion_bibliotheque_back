package Module;

public class LivrePreter {
    private int id;
    private int nbr;

    public LivrePreter(int id, int nbr) {
        this.id = id;
        this.nbr = nbr;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNbr() {
        return nbr;
    }

    public void setNbr(int nbr) {
        this.nbr = nbr;
    }
}
